// TC = O(1), SC = O(1)
#include <iostream>
using namespace std;

int main() {
    long long a, b;
    cin >> a >> b;
    
    cout << a <<" + " << b <<" = " << (a + b) <<"\n";
    cout << a <<" * " << b <<" = " << (a * b) <<"\n";
    cout << a <<" - " << b <<" = " << (a - b) <<"\n";
    
    return 0;
}
